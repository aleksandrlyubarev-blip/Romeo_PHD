const PHYSICS_NODE_TYPE_PREFIX = "physics_";

/**
 * Resolve the persona for a pipeline node by its declared type.
 * Returns null when the node has no dedicated persona (generic worker behavior).
 */
export function resolvePersonaForNodeType(nodeType: string): string | null {
  if (nodeType.startsWith(PHYSICS_NODE_TYPE_PREFIX)) {
    return PHYSICS_ENGINEER_PERSONA;
  }
  return null;
}
